#include "excution.h"

void    echo(int argc, char *argv[])
{
    int i;

    i = 0;
    if (argc > 1)
    {
        while (argv[i])
        {
            printf("%s",argv[i]);
            i++;
            if (!argv[i + 1])
                printf(" ");
        }
    }
}
