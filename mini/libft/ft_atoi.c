#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sing;
	int	j;

	i = 0;
	sing = 1;
	j = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		sing = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		j = j * 10 + str[i] - 48;
		i++;
	}
	return (j * sing);
}
